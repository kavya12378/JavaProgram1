package Java_Program;

public class if_statement {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        if(x>0) {
            System.out.println("x+y =" + (x+y));
        }
        if(x>0) {
            System.out.println("x-y =" + (x-y));
        }
        if(x>0) {
            System.out.println("x*y =" + (x*y));
        }
        if(x>0) {
            System.out.println("x/y =" + (x/y));
        }
        if(x>0) {
            System.out.println("x%y =" + (x%y));
        }
    }
}
